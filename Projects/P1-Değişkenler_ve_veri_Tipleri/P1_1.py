# Kullanıcıdan adını, yaşını ve doğum yılını alarak ekrana aşağıdaki gibi yazdıran bir Python programı yazın
name = input ("Lütfen adınızı giriniz: ")
surname = input ("Lütfen soyadınızı giriniz: ")
age = int(input("Lütfen yaşınızı giriniz: "))
birth_date = int(input("Lütfen doğum yılınızı giriniz: "))
print(f"Merhaba {name + surname}! {age} yaşındasın ve {birth_date} yılında doğmuşsun.")



